package com.example.fsaoutput.model;

public class jajanan {
    private int deskripsi;
    private int stock;
    private int photo;
    private String name;


    public jajanan() {
    }


    public int getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(int deskripsi) {
        this.deskripsi = deskripsi;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

}
